class MathClass {
    public static void main(String[] args) {
        double a = 28;
        double b = 4;
        double c = 30;

        System.out.println("maximum between a and b is :- "+Math.max(a, b));

        System.out.println("minimum between a and b is :- "+Math.min(a, b));

        System.out.println("square root of b is "+Math.sqrt(b));

        System.out.println("Logarithm of a is :- "+Math.log(a));
        System.out.println("Logarithm of b is :- "+Math.log(b));
        
        System.out.println("power of a to b :- "+Math.pow(a, b));

        System.out.println("sine of c :- "+Math.sin(c));

        System.out.println("cosine of c :- "+Math.cos(c));

        System.out.println("tan of c :- "+Math.tan(c));


    }    
}
