class TypeConversion {
    public static void main(String[] args) {
        int int_num = 100;
        long long_num = int_num;
        float float_num = int_num;
        
        System.out.println("integer value :- "+int_num);
        System.out.println("long value :- "+long_num);
        System.out.println("float value :- "+float_num);
    }    
}
